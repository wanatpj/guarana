Pawełek po wyszukaniu imion i nazwisk dziewczyn z V LO za pomocą szkolnego serwera, zapragnął poznać je bliżej. Zależy mu na zdobyciu dat urodzin tych dziewczyn. Paweł jest dżentelmenem i wie że dziewczyn o wiek się nie pyta, a szczególnie o datę urodzin ;) Wymyślił więc ciekawy sposób na zdobycie dat nie pytając o nie bezpośrednio. Pomysł wypalił i Pawełek zdobył co chciał, niestety po rozmowie z tyloma dziewczynami osłabł i zapomniał co dokładnie oznaczają liczby, które od nich otrzymał. Pomóż mu odzyskać daty urodzin dziewczyn z zapisków na jego liście. 
<a href=index.php?whereAmI=show&co=pawelek>Spytaj Pawła o jego sposób!</a>

<b>IN:</b>
Liczba naturalna n (0< n <100000), następnie n wierszy, a w każdym dwie liczby k i m o znaczeniu z treści zadania (Pawełek pamięta tylko, że 0< k <100).

<b>OUT:</b>
n wierszy, a w każdym data urodzin w formie: dd-mm-rr, gdzie dd oznacza dwucyfrową liczbę dnia, mm dwucyfrową liczbę miesiąca i rr dwie ostatnie cyfry roku urodzin.

<b>Przykład:</b>
in:
1 
77 149084
out: 
11-01-99