Dany jest ciąg:
01101001100...
Pierwsze dwa wyrazy są odpowiednio równe 0 1 (wyrazy numerujemy od 0). Kolejne wyrazy powstają w taki sposób, że do danego ciągu dopisuje się siebie samego znegowanego, tzn. dopisuje się ciąg taki jak jest aktualny tylko, że wszystkie 0 zastępujemy 1, a 1 zerami.
<b>Zadanie</b>
- wczytaj liczbę całkowitą dodatnią n <= 10^18 ze standardowego wejścia
- wyznacz i wypisz na standardowe wyjście n-ty wyraz podanego wyżej ciągu.
<u>Przykład:</u>
<b>Wejście:</b>
3
<b>Wyjście:</b>
0