Dany jest ciąg liczb. Znaleźć maksymalna wartość sumy spójnego podciągu (suma elementów po zbiorze pustym jest równa 0).
<b>Wejście:</b>
W pierwszej linii wejścia znajduje sie liczba całkowita n <=1000000 - długość ciągu. W drugiej linii wejścia znajduje n liczb całkowitych <= 10^9. Są to kolejne liczby danego ciągu. 
<b>Wyjście:</b>
Maksymalna wartość sumy spójnego podciągu danego ciągu. (Wynik jest mniejszy od 10^18)
<u>Przykład:</u>
<b>IN:</b>
3
1 2 3
<b>OUT:</b>
6