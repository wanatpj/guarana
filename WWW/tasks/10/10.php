Dany jest ciąg:
11000011...
Pierwsze dwa wyrazy to jedynki (wyrazy numerujemy od 1). Kolejne wyrazy powstają w taki sposób, że do danego ciągu dopisuje się siebie samego znegowanego, tzn. dopisuje się ciąg taki jak jest aktualny tylko, że wszystkie 0 zastępujemy 1, a 1 zerami.
<b>Zadanie</b>
- wczytaj liczbę całkowitą n <= 10^18 ze standardowego wejścia
- wyznacz i wypisz na standardowe wyjście n-ty wyraz podanego wyżej ciągu.
<u>Przykład:</u>
<b>Wejście:</b>
3
<b>Wyjście:</b>
0