<a href=\"http://pl.wikipedia.org/wiki/Problem_J%C3%B3zefa_Flawiusza\">Problem Józefa Flawiusza</a>
Zabijamy co drugą osobę.
Osoby numerujemy od 1 do n.
Najpierw zabijamy osobę 2.
J(n) - ostatnia osoba do zabicia w grupie n osobowej.
IN: 2 =< n <= 10^18
OUT: J(n)
1 test przykladowy:
IN: 5
OUT: 3
2 test przykladowy:
IN: 10
OUT: 5